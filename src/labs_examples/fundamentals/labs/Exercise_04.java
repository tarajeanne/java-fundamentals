package labs_examples.fundamentals.labs;

/**
 * Fundamentals Exercise 4: My Second Program
 *
 *      Write the necessary code to display, declare and print each of Java's primitive data types.
 *      Please declare a single variable of each type, give it an appropriate value, then print that variable.
 *
 */

public class Exercise_04 {

    public static void main(String[] args) {

        int i = 1;
        System.out.println("int i is: " + i);

        byte smallBoi = 123;
        short mediumBoi = 1000;
        int bigBoi = 450000;
        long longBoi = 1239876234293l;
        char littleBoi = 'z';
        boolean trueBoi = true;
        float impreciseBoi = 1.2345f;
        double preciseBoi = 1.234567891234;

        System.out.println("Who will win?");
        System.out.println("One small boi: " + smallBoi);
        System.out.println("One medium boi: " + mediumBoi);
        System.out.println("One big boi: " + bigBoi);
        System.out.println("One long boi: " + longBoi);
        System.out.println("One little boi: " + littleBoi);
        System.out.println("One true boi: " + trueBoi);
        System.out.println("One imprecise boi: " + impreciseBoi);
        System.out.println("One preciseBoi: " + preciseBoi);


    }

}
